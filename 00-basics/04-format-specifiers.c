#include <stdio.h>

int main()
{
    char dollar = '$';
    printf("Dollar sign is %c \n", dollar); // Format-specifier for character is %c

    int age = 25;
    printf("Age is %d \n", age); // Format-specifier for integers is %d

    float price = 132.5;
    printf("Price is %f \n", price); // Format-specifier for floating point numbers is %f

    return 0;
}